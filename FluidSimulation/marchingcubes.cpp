void GetVertexAddr(int i, int j, int k, int id, int &i1, int &j1, int &k1) {//the cube:[i:i+1,j:j+1,k:k+1]
	assert(0 <= id&&id < 8);
	i1 = i, j1 = j, k1 = k;
	if (id >= 4) {//4567 on the "top slice"
		k1++;
		id -= 4;
	}
	if (id == 1 || id == 2) i1++;
	if (id == 2 || id == 3) j1++;
}

void GetEdgeAddr(int i0, int j0, int k0, int id,int &i1,int &j1,int &k1,int &off) {
	assert(0 <= id&&id<12);
	i1 = i0, j1 = j0, k1 = k0;
	if (id == 0 || id == 2 || id == 4 || id == 6) {//x direction
		off = 0;
		if (id == 2 || id == 6) j1++;
		if (id == 4 || id == 6) k1++;
	}
	else if (id == 1 || id == 3 || id == 5 || id == 7) {//y direction
		off = 1;
		if (id == 1 || id == 5) i1++;
		if (id == 5 || id == 7) k1++;
	}
	else {//z direction
		off = 2;
		if (id == 9 || id == 10) i1++;
		if (id == 10 || id == 11) j1++;
	}
}

Float LinerInterpolate(Float x1, Float x2, Float val1, Float val2, Float isoval) {
	if (fabs(val1 - val2) < TSDF_EPS) {
		if (fabs(val1 - isoval) < TSDF_EPS) return x1;
		else return x2;
	}
	Float miu = (isoval - val1) / (val2 - val1);
	return x1 + miu*(x2 - x1);
}

