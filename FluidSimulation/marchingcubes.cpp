void MarchingCubes::List_Verts(const aryf & f, Float isoval) {
	int tot = 0;
	for (int i = 0; i < f.n; i++) {
		for (int j = 0; j < f.m; j++) {
			for (int k = 0; k < f.w; k++) {
				Float w1 = f.get(i, j, k);
				if (!finite(w1)) {
					w1 = 1;
				}
				int sgn1 = (w1 < isoval);
				for (int d = 0; d < 3; d++) {
					int i1 = i, j1 = j, k1 = k;
					if (d == 0) i1++;
					else if (d == 1) j1++;
					else if (d == 2) k1++;
					if (!f.inside(i1, j1, k1)) continue;
					Float w2 = f.get(i1, j1, k1);
					if (!finite(w2)) {
						w2 = 1;
					}
					int sgn2 = (w2 < isoval);
					if (sgn1 != sgn2) {
						Float x0 = LinerInterpolate(i, i1, w1, w2, isoval);
						Float y0 = LinerInterpolate(j, j1, w1, w2, isoval);
						Float z0 = LinerInterpolate(k, k1, w1, w2, isoval);
						verts.push_back(x0);
						verts.push_back(y0);
						verts.push_back(z0);
						if (d == 0) vertlisx(i, j, k) = tot;
						else if (d == 1) vertlisy(i, j, k) = tot;
						else if (d == 2) vertlisz(i, j, k) = tot;
						tot++;
					}
				}
			}
		}
	}
}

void MarchingCubes::List_Faces(const aryf &f, Float isoval) {
	for (int i = 0; i + 1 < f.n; i++) {
		for (int j = 0; j + 1 < f.m; j++) {
			for (int k = 0; k + 1 < f.w; k++) {
				//if (k < 59 || k>60) continue;
				int mask = 0;
				int i1, j1, k1;
				for (int d = 0; d < 8; d++) {
					GetVertexAddr(i, j, k, d, i1, j1, k1);
					Float w = f.get(i1, j1, k1);
					if (!finite(w)) {
						w = 1;
					}
					if (w < isoval) mask |= (1 << d);
				}
				int *tri = triTable[mask];
				for (int t = 0; tri[t] != -1; t += 3) {
					int vid;
					for (int d = 0; d<3; d++) {
						int off;
						GetEdgeAddr(i, j, k, tri[t + d], i1, j1, k1, off);
						if (off == 0) vid = vertlisx.get(i1, j1, k1);
						else if (off == 1) vid = vertlisy.get(i1, j1, k1);
						else if (off == 2) vid = vertlisz.get(i1, j1, k1);
						faces.push_back(vid + 1);//in .obj, vertices count from 1
					}
				}
			}
		}
	}
	assert(faces.size() % 3 == 0);
}

